/* Operador Lógico

&&   -> E
     true && true = true
     true && false = false
     false && false = false

||   -> OU
     true || true = true
     true || false = true
     false || false = false

!    -> Negação
     !true = false
     !false = true
 */


console.log(true && true)
console.log(!true || false)
