/*Operadores de Atribuição

=   -> Atribuição
+=  -> Adição
-=  -> Subtração
/=  -> Divisão
*=  -> Multiplicação
%=  -> Resto
 */

let numberOne = 10
console.log(numberOne)

numberOne += 2
console.log(numberOne)

numberOne -= 2
console.log(numberOne)

numberOne /= 2
console.log(numberOne)

numberOne *= 2
console.log(numberOne)

numberOne %= 3
console.log(numberOne)