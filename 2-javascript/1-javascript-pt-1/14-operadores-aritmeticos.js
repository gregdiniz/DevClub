/*Operadores Aritméticos
+  -> Adição
-  -> Subtração
*  -> Multiplicação
/  -> Divisão
%  -> Resto
++ -> Incremento
-- -> Decremento
** -> Exponecial
*/
//Traz o resto da divisão
console.log(7%3)

//Acrescenta 1 e -- descescenta
let myNumber = 10
console.log(++myNumber) //em myNumber++ primeiro o sistema traz o valor na variável e depois acresenta, então por esse motivo não funcionará em um console.log

//Exponenciação
console.log(2**3) //2³