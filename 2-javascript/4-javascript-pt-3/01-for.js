/*Estrutura de repetição - For 
    1- Inicialização - cria uma variável e coloca um valor inicial nela
    2- Condição - Enquando for true, o laço continuará iterando.
                  A condição será verificada antes de cada iteração.
    3- Expressão final - O que ocorrerá ao final de cada iteração

    for ([inicialização], [condição], [expressão final]){
        código a ser executado.
    }
*/

const listaChamada =["Carol", "Greg", "Pingo", "Jully"]

for(let i=0; i<listaChamada.length; i++){
    console.log(listaChamada[i])
    console.log('----------------')
}

